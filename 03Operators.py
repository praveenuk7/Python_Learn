# Mathematical operators
a = 11
b = 5

c = a+b
print(c)
c = a - b
print(c)
c = a/b
print(c)
c = a % b
print(c)
c = a ** b
print(c)
c = a // b
print(c)

# Assignment operators
x = 10

x += 4
print(x)
x -= 4
print(x)
x *= 4
print(x)

# Comparison operator
a = 20
b = 20

print(a == b)
print(a != b)
print(a > b)
print(a < b)
print(a >= b)
print(a <= b)

# Logical operators
a = 10

print(a == 10 and a < 15)
print(a == 5 or a > 11)
print(not(a == 10))

